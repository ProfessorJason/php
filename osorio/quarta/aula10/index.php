	<h1>Aula 10 - Laços de Repetição</h1>

	<p>
		Laços (ou estruturas) de repetição são códigos específicos na programação que nos permitem executar determinados comandos repetidas vezes. <br>
		Enquanto a condição de execução destes laços for <b>verdadeira</b>, os comandos especificados dentro das chaves serão executados. Quando a condição passar a ser <b>falsa</b>, o laço de repetição é encerrado e o programa retoma sua execução normal a partir do fim da estrutura.
	</p>

	<p>
		No PHP, trabalhamos basicamente com 4 tipos de laços: <br>

		<ul>
			<li><a href="for/index.php">FOR (PARA)</a></li>
			<li><a href="while/index.php">WHILE (ENQUANTO)</a></li>
			<li>DO / WHILE (FAÇA / ENQUANTO)</li>
			<li>FOREACH (PARA CADA - PRÓPRIO PARA ARRAYS)</li>
		</ul>
	</p>

