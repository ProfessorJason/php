	<p>
		O laço de repetição <i>for</i> (para) é o mais utilizado, de forma geral, na programação. Ele possui uma sintaxe bem completa que nos permite ter o controle sobre todas as etapas da execução de um laço:

		<ul>
			<li>Qual a variável de controle (e o seu valor inicial)</li>
			<li>Qual a condição de execução do laço</li>
			<li>Qual o incremento/decremento da variável de controle a cada execução do laço</li>
		</ul>
	</p>

	<h3>Sintaxe</h3>

	<pre>
for (valor ini. do cont.; condição de exe.; incremento/decremento do cont.)
{
  < comandos >
}
	</pre>

